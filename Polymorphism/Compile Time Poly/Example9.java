class Example9
{
	/*
		Java does not support the operator overloading except + operator.

		The + operator has more than one behavior like Addition and Concatination
	*/
	public static void main(String args[])
	{
		System.out.println(52+65);						// Perform Addition
		System.out.println("I am "+"Sahil Shaikh");		// Perform Concatination
		System.out.println("My roll num is : "+101);	// Perform Concatination
	}
}