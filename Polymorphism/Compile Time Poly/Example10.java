class Example10
{
	/*
		Java does not support the operator overloading except + operator.

		The + operator has more than one behavior like Addition and Concatination
	*/
	public static void main(String args[])
	{
		System.out.println(20+1);										// Perform Addition
		System.out.println(101+" I am "+"Sahil Shaikh");				// Perform Concatination
		System.out.println("My roll num is : "+101+" in college.");		// Perform Concatination
	}
}