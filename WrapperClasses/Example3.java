class Example3
{
	public static void main(String[] args)
	{
		Float f1=new Float(202.5);
		System.out.println(f1);
		Float f2=new Float(202.5f);
		System.out.println(f1);
		Float f3=new Float("202.5");
		System.out.println(f1);
		Character c=new Character('A');
		System.out.println(c);
	}
}