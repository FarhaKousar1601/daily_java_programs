class Example7
{
	public static void main(String[] args)
	{
		String str1="10";					// string value
		String str2="20";					// string value
		int num1=Integer.parseInt(str1);	// convert string to integer
		int num2=Integer.parseInt(str2);	// convert string to integer
		System.out.println(num1);			// int value
		System.out.println(num2);			// int value
		System.out.println(num1+num2);		// 30
	}
}